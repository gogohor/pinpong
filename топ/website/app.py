@app.route('/', methods=['GET', 'POST'])
def register():
    if request.method == 'POST':
        username = request.form['username']
        password = request.form['password']
        
        
        if any(user['username'] == username for user in users):
            return render_template('register.html', error='Користувач з таким ім\'ям вже існує!')
        
        
        users.append({'username': username, 'password': password})
        return redirect(url_for('login'))
    return render_template('register.html')



@app.route('/login', methods=['GET', 'POST'])
def login():
    if request.method == 'POST':
        username = request.form['username']
        password = request.form['password']
        
        for user in users:
            if user['username'] == username and user['password'] == password:
                return render_template('home.html')
        return render_template('login.html', error='Неправильне ім\'я користувача або пароль!')
    return render_template('login.html')




@app.route('/submit', methods=['POST'])
def submit():
    score = 0
    for question in questions:
        selected_option = request.form.get(question['question'])
        if selected_option == question['correct_answer']:
            score += 1
    return render_template('result.html')
    
